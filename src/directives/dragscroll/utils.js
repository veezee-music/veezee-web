export default {
    addEventListeners (el, events, handler) {
        for (var i = 0, len = events.length; i < len; i++) {
            el.addEventListener(events[i], handler)
        }
    },

    removeEventListeners (el, events, handler) {
        for (var i = 0, len = events.length; i < len; i++) {
            el.removeEventListener(events[i], handler)
        }
    }
}